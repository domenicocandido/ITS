def print_seq(): 
    
    print("Sequenza a):")
    for i in range(1, 8):
        print(i)

    print("\nSequenza b):")
    for i in range(3, 24, 5):
        print(i)

    print("\nSequenza c):")
    for i in range(20 , -11, -6):
        print(i)

    print("\nSequenza d):")
    for i in range(19, 52, 8):
        print(i)
    
    return

print_seq()
